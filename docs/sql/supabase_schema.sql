-- Create Profiles Table (Linked to Auth)
create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  name text,
  email text,
  cpf text,
  phone text,
  address_zip text,
  address_street text,
  address_number text,
  address_complement text,
  address_district text,
  address_city text,
  address_state text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS on profiles
alter table public.profiles enable row level security;

-- Policies for Profiles
drop policy if exists "Users can view own profile" on profiles;
create policy "Users can view own profile"
  on profiles for select
  using ( auth.uid() = id );

drop policy if exists "Users can update own profile" on profiles;
create policy "Users can update own profile"
  on profiles for update
  using ( auth.uid() = id );

drop policy if exists "Users can insert own profile" on profiles;
create policy "Users can insert own profile"
  on profiles for insert
  with check ( auth.uid() = id );

-- Create Products Table
create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  price decimal(10,2) not null,
  image text not null,
  category text not null,
  description text,
  sizes text[] default '{}',
  colors text[] default '{}',
  rating decimal(2,1) default 5.0,
  reviews integer default 0,
  is_new boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable Row Level Security (RLS)
alter table public.products enable row level security;

-- Policies for Products
drop policy if exists "Public Products are viewable by everyone" on products;
create policy "Public Products are viewable by everyone"
  on products for select
  using ( true );

drop policy if exists "Enable insert for everyone" on products;
create policy "Enable insert for everyone"
  on products for insert
  with check ( true );

drop policy if exists "Enable update for everyone" on products;
create policy "Enable update for everyone"
  on products for update
  using ( true );

drop policy if exists "Enable delete for everyone" on products;
create policy "Enable delete for everyone"
  on products for delete
  using ( true );


