-- Create Orders Table
create table if not exists public.orders (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users(id), -- Linked to User
  customer_name text not null,
  customer_phone text,
  total decimal(10,2) not null,
  status text default 'Pendente',
  payment_method text not null,
  address text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS on orders
alter table public.orders enable row level security;

-- Policies for Orders
drop policy if exists "Users can view own orders" on orders;
create policy "Users can view own orders"
  on orders for select
  using ( auth.uid() = user_id );

-- Add Payment and Shipping Columns to Store Settings
alter table public.store_settings add column if not exists pix_key text;
alter table public.store_settings add column if not exists pix_owner_name text;
alter table public.store_settings add column if not exists pix_owner_city text;
alter table public.store_settings add column if not exists fixed_shipping_price numeric default 15.00;


drop policy if exists "Users can insert own orders" on orders;
create policy "Users can insert own orders"
  on orders for insert
  with check ( auth.uid() = user_id );
  
drop policy if exists "Enable update for everyone" on orders;
create policy "Enable update for everyone"
  on orders for update
  using ( true );

-- Create Order Items Table
create table if not exists public.order_items (
  id bigint generated by default as identity primary key,
  order_id uuid references public.orders(id) on delete cascade,
  product_name text not null,
  quantity integer not null,
  size text,
  price decimal(10,2) not null,
  image text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS on order_items
alter table public.order_items enable row level security;

-- Policies for Order Items
drop policy if exists "Users can view own order items" on order_items;
create policy "Users can view own order items"
  on order_items for select
  using ( exists ( select 1 from public.orders where orders.id = order_items.order_id and orders.user_id = auth.uid() ) );

drop policy if exists "Users can insert own order items" on order_items;
create policy "Users can insert own order items"
  on order_items for insert
  with check ( exists ( select 1 from public.orders where orders.id = order_items.order_id and orders.user_id = auth.uid() ) );

